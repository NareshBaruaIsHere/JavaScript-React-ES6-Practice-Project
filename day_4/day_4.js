// Simple loop example
for (let i = 1; i <= 5; i++) {
  console.log("Count:", i);
}

// While loop example
let j = 1;
while (j <= 3) {
  console.log("While:", j);
  j++;
}

// Do...while loop example
let k = 1;
do {
  console.log("Do while:", k);
  k++;
} while (k <= 3);

// For...of loop example
const fruits = ["apple", "banana", "mango"];
for (const fruit of fruits) {
  console.log("Fruit:", fruit);
}
